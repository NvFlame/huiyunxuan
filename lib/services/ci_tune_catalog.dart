final Set<String> ciTuneCatalogNames = _parseCiTuneCatalog(ciTuneCatalogSource);

Set<String> _parseCiTuneCatalog(String source) {
  final names = <String>{};
  for (final line in source.split('\n')) {
    final normalized = normalizeCiTuneName(line);
    if (normalized.isNotEmpty) {
      names.add(normalized);
    }
  }
  return Set.unmodifiable(names);
}

String normalizeCiTuneName(String value) {
  return value
      .replaceAll('《', '')
      .replaceAll('》', '')
      .replaceAll(RegExp(r'\s+'), '')
      .trim();
}

bool titleMatchesCiTune(String normalizedTitle, String tuneName) {
  final normalizedTune = normalizeCiTuneName(tuneName);
  return normalizedTitle == normalizedTune ||
      normalizedTitle.startsWith('$normalizedTune·') ||
      normalizedTitle.startsWith('$normalizedTune•') ||
      normalizedTitle.startsWith('$normalizedTune・');
}
